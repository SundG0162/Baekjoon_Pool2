#define MEMSET(arr, i) memset(arr, i, sizeof(arr))

int main()
{
	int n;
	cin >> n;
	vector<int> a(n), b(n);
	for (int i = 0; i < n; i++)
		cin >> a[i];
	for (int i = 0; i < n; i++)
		cin >> b[i];

	std::sort(a.begin(), a.end(), std::greater<int>());
	std::sort(b.begin(), b.end(), std::less<int>());

	int s = 0;
	for (int i = 0; i < n; i++)
	{
		s += a[i] * b[i];
	}
	cout << s;
}